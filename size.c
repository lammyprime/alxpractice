#include <stdio.h>
int main()
{
  long long int c;
  printf("size of char: %ldbyte(s)\n", sizeof(char));
  printf("size of int: %ldbytes\n", sizeof(int));
  printf("size of float: %ldbytes\n", sizeof(float));
  printf("size of long int: %ldbytes\n", sizeof(long int));
  printf("size of long long int: %ldbytes\n", sizeof(c));
  return (0);
}
